.section .text
.globl _start
.org 0x0

_start:
    # ============================================================
    # 1. 基础初始化
    # ============================================================
    addi x1, x0, 15        # x1 = 15
    addi x2, x0, 3         # x2 = 3
    addi x3, x0, -16       # x3 = 0xfffffff0

    # ============================================================
    # 2. R 型指令
    # ============================================================
    add  x4, x1, x2        # x4 = 18
    sub  x5, x4, x2        # x5 = 15
    and  x6, x4, x5        # x6 = 2
    or   x7, x6, x2        # x7 = 3
    xor  x8, x4, x5        # x8 = 29
    sll  x9, x2, x2        # x9 = 24
    srl  x10, x9, x2       # x10 = 3
    sra  x11, x3, x2       # x11 = -2
    slt  x12, x3, x1       # x12 = 1
    sltu x13, x3, x1       # x13 = 0

    # ============================================================
    # 3. I 型运算指令
    # ============================================================
    addi  x14, x1, 5       # x14 = 20
    andi  x15, x1, 7       # x15 = 7
    ori   x16, x15, 8      # x16 = 15
    xori  x17, x16, 10     # x17 = 5
    slti  x18, x3, 0       # x18 = 1
    sltiu x19, x3, 1       # x19 = 0
    slli  x20, x1, 2       # x20 = 60
    srli  x21, x20, 2      # x21 = 15
    srai  x22, x3, 2       # x22 = -4

    # ============================================================
    # 4. lw / sw 与 load-use
    # ============================================================
    sw x14, 0(x0)          # mem[0] = 20
    lw x23, 0(x0)          # x23 = 20
    add x24, x23, x14      # x24 = 40，测试 load-use
    sw x24, 4(x0)          # mem[4] = 40
    lw x25, 4(x0)          # x25 = 40

    # ============================================================
    # 5. sb / lb / lbu
    # ============================================================
    addi x5, x0, -1        # x5 = 0xffffffff
    sb   x5, 8(x0)         # mem[8] = 0xff
    lb   x6, 8(x0)         # x6 = 0xffffffff
    addi x11, x6, 1        # x11 = 0，测试 lb load-use
    lbu  x7, 8(x0)         # x7 = 0x000000ff

    # ============================================================
    # 6. sh / lh / lhu
    # ============================================================
    addi x8, x0, -128      # x8 = 0xffffff80
    sh   x8, 12(x0)        # mem[12:13] = 0xff80
    lh   x9, 12(x0)        # x9 = 0xffffff80
    lhu  x10, 12(x0)       # x10 = 0x0000ff80
    add  x13, x10, x11     # x13 = 0x0000ff80，测试 lhu 后使用

    # ============================================================
    # 7. B 型分支 + flush
    # ============================================================
    beq x14, x23, BEQ_OK
    addi x29, x0, 101      # 错误路径，应被 flush
    addi x29, x0, 102      # 错误路径，应被 flush

BEQ_OK:
    bne x14, x25, BNE_OK
    addi x29, x0, 103      # 错误路径，应被 flush
    addi x29, x0, 104      # 错误路径，应被 flush

BNE_OK:
    blt x3, x1, BLT_OK
    addi x29, x0, 105      # 错误路径，应被 flush
    addi x29, x0, 106      # 错误路径，应被 flush

BLT_OK:
    bltu x1, x3, BLTU_OK
    addi x29, x0, 107      # 错误路径，应被 flush
    addi x29, x0, 108      # 错误路径，应被 flush

BLTU_OK:
    bge x1, x3, BGE_OK
    addi x29, x0, 109      # 错误路径，应被 flush
    addi x29, x0, 110      # 错误路径，应被 flush

BGE_OK:
    bgeu x3, x1, BGEU_OK
    addi x29, x0, 111      # 错误路径，应被 flush
    addi x29, x0, 112      # 错误路径，应被 flush

BGEU_OK:
    # ============================================================
    # 8. jal 测试
    # jal 地址是 220，所以 x26 = PC + 4 = 224 = 0xe0
    # ============================================================
    jal x26, JAL_OK

    addi x29, x0, 113      # 错误路径，应被 flush
    addi x29, x0, 114      # 错误路径，应被 flush

JAL_OK:
    addi x27, x0, 1        # x27 = 1

    # ============================================================
    # 9. jalr 测试
    # x26 = 224
    # x30 = 224 + 28 = 252
    # jalr 地址是 240，所以 x28 = PC + 4 = 244 = 0xf4
    # ============================================================
    addi x30, x26, 28
    jalr x28, x30, 0

    addi x29, x0, 115      # 错误路径，应被 flush
    addi x29, x0, 116      # 错误路径，应被 flush

JALR_OK:
    # ============================================================
    # 10. U 型指令
    # ============================================================
    lui x5, 0x12345        # x5 = 0x12345000

    auipc x12, 0x00000     # 当前 PC = 256 = 0x100，所以 x12 = 0x100
    addi  x12, x12, 8      # x12 = 0x108

    # ============================================================
    # 11. 最终错误路径检查
    # ============================================================
    beq x29, x0, end
    addi x31, x0, 99       # 如果执行到这里，说明 flush 有问题

end:
    beq x0, x0, end