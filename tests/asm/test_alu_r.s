    # ----------------------------------------------------------------
    # Test 1: add — basic addition
    # ----------------------------------------------------------------
    addi x1, x0, 15          # x1 = 15
    addi x2, x0, 3           # x2 = 3
    add  x3, x1, x2          # x3 = 18
    addi x4, x0, 18
    bne  x3, x4, fail_101

    # ----------------------------------------------------------------
    # Test 2: add — with negative
    # ----------------------------------------------------------------
    addi x1, x0, -10         # x1 = 0xfffffff6
    addi x2, x0, 5           # x2 = 5
    add  x3, x1, x2          # x3 = -5
    addi x4, x0, -5
    bne  x3, x4, fail_102

    # ----------------------------------------------------------------
    # Test 3: sub — basic
    # ----------------------------------------------------------------
    addi x1, x0, 20
    addi x2, x0, 7
    sub  x3, x1, x2          # x3 = 13
    addi x4, x0, 13
    bne  x3, x4, fail_103

    # ----------------------------------------------------------------
    # Test 4: sub — producing negative
    # ----------------------------------------------------------------
    addi x1, x0, 3
    addi x2, x0, 10
    sub  x3, x1, x2          # x3 = -7
    addi x4, x0, -7
    bne  x3, x4, fail_104

    # ----------------------------------------------------------------
    # Test 5: and — bitwise
    # ----------------------------------------------------------------
    addi x1, x0, 0x0F        # x1 = 0b00001111
    addi x2, x0, 0x33        # x2 = 0b00110011
    and  x3, x1, x2          # x3 = 0b00000011 = 3
    addi x4, x0, 3
    bne  x3, x4, fail_105

    # ----------------------------------------------------------------
    # Test 6: or — bitwise
    # ----------------------------------------------------------------
    addi x1, x0, 0x0F        # x1 = 0b00001111
    addi x2, x0, 0x30        # x2 = 0b00110000
    or   x3, x1, x2          # x3 = 0b00111111 = 63
    addi x4, x0, 63
    bne  x3, x4, fail_106

    # ----------------------------------------------------------------
    # Test 7: xor — bitwise
    # ----------------------------------------------------------------
    addi x1, x0, 0x0F        # x1 = 0b00001111
    addi x2, x0, 0x33        # x2 = 0b00110011
    xor  x3, x1, x2          # x3 = 0b00111100 = 60
    addi x4, x0, 60
    bne  x3, x4, fail_107

    # ----------------------------------------------------------------
    # Test 8: slt — negative < positive = 1
    # ----------------------------------------------------------------
    addi x1, x0, -5
    addi x2, x0, 10
    slt  x3, x1, x2          # -5 < 10 → 1
    addi x4, x0, 1
    bne  x3, x4, fail_108

    # ----------------------------------------------------------------
    # Test 9: slt — positive < negative = 0
    # ----------------------------------------------------------------
    addi x1, x0, 10
    addi x2, x0, -5
    slt  x3, x1, x2          # 10 < -5 → 0
    bne  x3, x0, fail_109

    # ----------------------------------------------------------------
    # Test 10: slt — equal = 0
    # ----------------------------------------------------------------
    addi x1, x0, 7
    addi x2, x0, 7
    slt  x3, x1, x2          # 7 < 7 → 0
    bne  x3, x0, fail_110

    # ----------------------------------------------------------------
    # Test 11: sltu — unsigned comparison
    #   0xFFFFFFFF (unsigned max) > 1, so 1 < 0xFFFFFFFF → 1
    # ----------------------------------------------------------------
    addi x1, x0, 1
    addi x2, x0, -1          # x2 = 0xFFFFFFFF (unsigned max)
    sltu x3, x1, x2          # 1 < 4294967295 → 1
    addi x4, x0, 1
    bne  x3, x4, fail_111

    # ----------------------------------------------------------------
    # Test 12: sltu — reverse
    # ----------------------------------------------------------------
    addi x1, x0, -1          # x1 = 0xFFFFFFFF (unsigned max)
    addi x2, x0, 1
    sltu x3, x1, x2          # 4294967295 < 1 → 0
    bne  x3, x0, fail_112

    # ----------------------------------------------------------------
    # Test 13: sll — shift left logical
    # ----------------------------------------------------------------
    addi x1, x0, 3           # x1 = 0b0011
    addi x2, x0, 2           # shift amount = 2
    sll  x3, x1, x2          # x3 = 0b1100 = 12
    addi x4, x0, 12
    bne  x3, x4, fail_113

    # ----------------------------------------------------------------
    # Test 14: sll — shift by 0
    # ----------------------------------------------------------------
    addi x1, x0, 42
    sll  x3, x1, x0          # shift by x0=0 → no change
    addi x4, x0, 42
    bne  x3, x4, fail_114

    # ----------------------------------------------------------------
    # Test 15: srl — shift right logical
    # ----------------------------------------------------------------
    addi x1, x0, 0x0C        # x1 = 0b1100 = 12
    addi x2, x0, 2
    srl  x3, x1, x2          # x3 = 0b0011 = 3
    addi x4, x0, 3
    bne  x3, x4, fail_115

    # ----------------------------------------------------------------
    # Test 16: sra — shift right arithmetic (preserves sign)
    #   -16 = 0xFFFFFFF0, shifted right by 2 = 0xFFFFFFFC = -4
    # ----------------------------------------------------------------
    addi x1, x0, -16
    addi x2, x0, 2
    sra  x3, x1, x2          # -16 >> 2 (arithmetic) = -4
    addi x4, x0, -4
    bne  x3, x4, fail_116

    # ----------------------------------------------------------------
    # Test 17: sra — positive number (should behave like srl)
    # ----------------------------------------------------------------
    addi x1, x0, 16
    addi x2, x0, 2
    sra  x3, x1, x2          # 16 >> 2 = 4
    addi x4, x0, 4
    bne  x3, x4, fail_117

    # ----------------------------------------------------------------
    # Test 18: add with x0 destination (should be ignored by regfile)
    #    We read x0 back — should still be 0
    # ----------------------------------------------------------------
    addi x1, x0, 99
    add  x0, x1, x1          # write 198 to x0 (hardwired to 0)
    add  x3, x0, x0          # x3 = x0 + x0 = 0
    bne  x3, x0, fail_118

    # ----------------------------------------------------------------
    # All tests passed
    # ----------------------------------------------------------------
pass:
    addi x28, x0, 1
    sw   x28, 0x3F0(x0)
    beq  x0, x0, pass

