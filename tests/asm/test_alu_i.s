    # ----------------------------------------------------------------
    addi x1, x0, 0xFF         # x1 = 0b11111111
    andi x3, x1, 0x0F         # x3 = 0b00001111 = 15
    addi x4, x0, 15
    bne  x3, x4, fail_205

    # ----------------------------------------------------------------
    # Test 6: andi — with 12-bit mask
    # ----------------------------------------------------------------
    addi x1, x0, 0x7FF        # x1 = 0x000007FF
    andi x3, x1, 0x0F0        # 0b011111111111 & 0b000011110000 = 0b000011110000 = 240
    addi x4, x0, 240
    bne  x3, x4, fail_206

    # ----------------------------------------------------------------
    # Test 7: ori — bitwise OR
    # ----------------------------------------------------------------
    addi x1, x0, 0x0F0        # x1 = 0b000011110000
    ori  x3, x1, 0x00F        # x3 = 0b000011111111 = 255
    addi x4, x0, 255
    bne  x3, x4, fail_207

    # ----------------------------------------------------------------
    # Test 8: xori — bitwise XOR
    # ----------------------------------------------------------------
    addi x1, x0, 0xFF         # x1 = 0b11111111
    xori x3, x1, 0x0F         # 0b11111111 XOR 0b00001111 = 0b11110000 = 240
    addi x4, x0, 240
    bne  x3, x4, fail_208

    # ----------------------------------------------------------------
    # Test 9: slti — signed less than, negative < positive
    # ----------------------------------------------------------------
    addi x1, x0, -5
    slti x3, x1, 10           # -5 < 10 → 1
    addi x4, x0, 1
    bne  x3, x4, fail_209

    # ----------------------------------------------------------------
    # Test 10: slti — signed, positive < negative
    # ----------------------------------------------------------------
    addi x1, x0, 10
    slti x3, x1, -5           # 10 < -5 → 0
    bne  x3, x0, fail_210

    # ----------------------------------------------------------------
    # Test 11: slti — equal values
    # ----------------------------------------------------------------
    addi x1, x0, 7
    slti x3, x1, 7            # 7 < 7 → 0
    bne  x3, x0, fail_211

    # ----------------------------------------------------------------
    # Test 12: sltiu — unsigned: 0xFFFFFFFF NOT less than 1
    # ----------------------------------------------------------------
    addi x1, x0, -1           # x1 = 0xFFFFFFFF
    sltiu x3, x1, 1           # 0xFFFFFFFF < 1 → 0
    bne  x3, x0, fail_212

    # ----------------------------------------------------------------
    # Test 13: sltiu — unsigned: 0 < 5
    # ----------------------------------------------------------------
    sltiu x3, x0, 5           # 0 < 5 → 1
    addi x4, x0, 1
    bne  x3, x4, fail_213

    # ----------------------------------------------------------------
    # Test 14: slli — shift left logical
    # ----------------------------------------------------------------
    addi x1, x0, 3            # x1 = 0b0011
    slli x3, x1, 2            # x3 = 0b1100 = 12
    addi x4, x0, 12
    bne  x3, x4, fail_214

    # ----------------------------------------------------------------
    # Test 15: slli — shift by max (31)
    # ----------------------------------------------------------------
    addi x1, x0, 1
    slli x3, x1, 31           # 1 << 31 = 0x80000000
    addi x4, x0, 1
    slli x4, x4, 31
    bne  x3, x4, fail_215

    # ----------------------------------------------------------------
    # Test 16: srli — shift right logical
    # ----------------------------------------------------------------
    addi x1, x0, 12           # x1 = 0b1100
    srli x3, x1, 2            # x3 = 0b0011 = 3
    addi x4, x0, 3
    bne  x3, x4, fail_216

    # ----------------------------------------------------------------
    # Test 17: srai — shift right arithmetic (preserves sign)
    # ----------------------------------------------------------------
    addi x1, x0, -16          # x1 = 0xFFFFFFF0
    srai x3, x1, 2            # x3 = 0xFFFFFFFC = -4
    addi x4, x0, -4
    bne  x3, x4, fail_217

    # ----------------------------------------------------------------
    # All tests passed
    # ----------------------------------------------------------------
